void main() {
  int x = 5;
  int y = 10;

  bool isEqual = x == y; // Equal to
  bool isNotEqual = x != y; // Not equal to
  bool isLessThan = x < y; // Less than
  bool isGreaterThan = x > y; // Greater than
  bool isLessOrEqual = x <= y; // Less than or equal to
  bool isGreaterOrEqual = x >= y; // Greater than or equal to

  print('Is Equal: $isEqual');
  print('Is Not Equal: $isNotEqual');
  print('Is Less Than: $isLessThan');
  print('Is Greater Than: $isGreaterThan');
  print('Is Less or Equal: $isLessOrEqual');
  print('Is Greater or Equal: $isGreaterOrEqual');
}
